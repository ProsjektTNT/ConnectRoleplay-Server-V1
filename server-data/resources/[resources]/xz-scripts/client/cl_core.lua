XZCore = nil
local PlayerData = {}

Citizen.CreateThread(function()
    while XZCore == nil do
        TriggerEvent('XZCore:GetObject', function(obj) XZCore = obj end)
        Citizen.Wait(500)
    end

    while XZCore.Functions.GetPlayerData().job == nil do
        Citizen.Wait(100)
    end

    Citizen.Wait(800)
    PlayerData = XZCore.Functions.GetPlayerData() -- Setting PlayerData vars
    TriggerServerEvent("Scripts:ready")
end)

RegisterNetEvent("XZCore:Client:OnPlayerLoaded")
AddEventHandler("XZCore:Client:OnPlayerLoaded", function(Player)
    PlayerData = Player
end)

